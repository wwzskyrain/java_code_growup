package jdk.util;

public class SwitchCase {


    public static void main(String[] args) {

        switch (2) {

            case 1:
                System.out.println("case 1");

            case 2:
                System.out.println("case 2");

            case 3:
                System.out.println("case 3");

            case 4:
                System.out.println("case 4");
                break;
            default:
                System.out.println("default case");
        }

    }

}
